void des_key_schedule(int key[64], int rk[16][48])
{
	int pc1Table[56] = { 57,49,41,33,25,17,9,1,
	58,50,42,34,26,18,10,2,
	59,51,43,35,27,19,11,3,
	60,52,44,36,63,55,47,39,
	31,23,15,7,62,54,46,38,
	30,22,14,6,61,53,45,37,
	29,21,13,5,28,20,12,4 };
	int pc2Table[48] = { 14,17,11,24,1,5,
	3,28,15,6,21,10,
	23,19,12,4,26,8,
	16,7,27,20,13,2,
	41,52,31,37,47,55,
	30,40,51,45,33,48,
	44,49,39,56,34,53,
	46,42,50,36,29,32 };
	int i, r, tmp[56];
	for (i = 0;i < 56;i++)
	{
		tmp[i] = key[pc1Table[i]-1];
	}//pc1
	int C[28], D[28];
	for (r = 1;r <= 16;r++)
	{
		for (i = 0;i < 28;i++)
		{
			C[i] = tmp[i];
			D[i] = tmp[i + 28];
		}
		if (r == 1 || r == 2 || r == 9 || r == 16)
		{
			int t[2] = { C[0],D[0] };
			for (i = 0;i < 27;i++)
			{
				C[i] = C[i + 1];
				D[i] = D[i + 1];
			}
			C[27] = t[0];
			D[27] = t[1];
		}
		else {
			int t[4] = { C[0],C[1],D[0],D[1] };
			for (i = 0;i < 26;i++)
			{
				C[i] = C[i + 2];
				D[i] = D[i + 2];
			}
			C[26] = t[0];
			C[27] = t[1];
			D[26] = t[2];
			D[27] = t[3];
		}
		for (i = 0;i < 28;i++)
		{
			tmp[i] = C[i];
			tmp[i + 28] = D[i];
		}
		for (i = 0;i < 48;i++)
		{
			rk[r - 1][i] = tmp[pc2Table[i]-1];
		}
		printf("第%d轮密钥为：\n", r);
		for (int i = 0;i < 48;i++)
		{
			printf("%d", rk[r-1][i]);
		}
		printf("\n");
	}
}

